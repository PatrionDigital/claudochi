/* Zero all persistable stats back to a fresh-pet state. Must hold mtx.
 *
 * Deliberately does NOT perform I/O (persist_save), notification
 * sequences, or ensure_anim here. An earlier version called all three
 * inline; that froze the device — notification_message on
 * sequence_double_vibro blocks ~400 ms while the buzzer plays,
 * storage_file_open on SD can take 50-200 ms, and icon_animation_start
 * touches the FreeRTOS timer-service queue. All three under the same
 * mutex held by the input task racing against the draw/BT/RX paths was
 * enough to stall everything.
 *
 * The caller flushes to SD and fires notifications AFTER releasing
 * mtx. The next draw tick (≤ 250 ms) re-runs ensure_anim with the
 * freshly-zeroed age, which naturally swaps in the egg sprite. */
static void claudegotchi_reset(ClaudeBuddyApp* app) {
    app->play_level = 0;
    app->feed_level = 0;
    app->age_transactions = 0;
    app->total_approvals = 0;
    app->total_denials = 0;
    app->interaction_bytes = 0;
    app->approval_streak = 0;
    app->denial_streak = 0;
    app->pending_msg_ts = 0;
    app->pending_msg_bytes = 0;
    app->pending_start_tokens = 0;
    app->last_activity_ms = 0;
    app->lonely_since_ms = 0;
    app->starving_since_ms = 0;
    app->last_decay_ms = 0;
    app->tokens_baseline_set = false;
    app->last_celebrated_tokens = 0;
    app->transient_state = PetStateSleep;
    app->transient_until_ms = 0;
    app->konami_progress = 0;
    /* Clear msg so the bars/age panel renders on the next draw —
     * otherwise a sticky status label ("RUN yarn" etc) would hide the
     * reset confirmation until the desktop sends a new heartbeat. */
    app->hb_msg[0] = '\0';
}

static void persist_save(ClaudeBuddyApp* app) {
    Storage* storage = furi_record_open(RECORD_STORAGE);
    storage_simply_mkdir(storage, CB_STATE_DIR);

    ClaudeBuddyPersist state = {
        .magic = CB_STATE_MAGIC,
        .version = CB_STATE_VERSION,
        .play_level = app->play_level,
        .feed_level = app->feed_level,
        .age_transactions = app->age_transactions,
        .total_approvals = app->total_approvals,
        .total_denials = app->total_denials,
        .interaction_bytes = app->interaction_bytes,
        .last_saved_ms = furi_get_tick(),
    };
    memset(state.reserved, 0, sizeof(state.reserved));

    File* f = storage_file_alloc(storage);
    if(storage_file_open(f, CB_STATE_PATH, FSAM_WRITE, FSOM_CREATE_ALWAYS)) {
        storage_file_write(f, &state, sizeof(state));
        storage_file_close(f);
    }
    storage_file_free(f);
    furi_record_close(RECORD_STORAGE);

    app->last_save_ms = furi_get_tick();
}

static void persist_load(ClaudeBuddyApp* app) {
    Storage* storage = furi_record_open(RECORD_STORAGE);
    File* f = storage_file_alloc(storage);
    if(storage_file_open(f, CB_STATE_PATH, FSAM_READ, FSOM_OPEN_EXISTING)) {
        ClaudeBuddyPersist state;
        size_t n = storage_file_read(f, &state, sizeof(state));
        storage_file_close(f);
        if(n == sizeof(state) && state.magic == CB_STATE_MAGIC &&
           state.version == CB_STATE_VERSION) {
            app->play_level = state.play_level;
            app->feed_level = state.feed_level;
            app->age_transactions = state.age_transactions;
            app->total_approvals = state.total_approvals;
            app->total_denials = state.total_denials;
            app->interaction_bytes = state.interaction_bytes;
        }
        /* else: stale magic/version → keep zeros (fresh pet). */
    }
    storage_file_free(f);
    furi_record_close(RECORD_STORAGE);
}

/* Called when an unrecognized msg arrives. First resolves any prior
 * pending entry that's older than CLASSIFY_WINDOW_MS — otherwise we'd
 * lose classification of that earlier entry. Then stamps the new one. */
static void gotchi_record_msg_change(ClaudeBuddyApp* app, uint32_t bytes) {
    uint32_t now = furi_get_tick();
    if(app->pending_msg_ts && now - app->pending_msg_ts > CLASSIFY_WINDOW_MS) {
        gotchi_classify_pending(app);
    } else if(app->pending_msg_ts) {
        /* New input overrides old within window — treat the old one
         * as "ambiguous" and classify based on tokens so far. */
        gotchi_classify_pending(app);
    }
    app->pending_msg_ts = now;
    app->pending_msg_bytes = bytes;
    app->pending_start_tokens = app->hb_tokens;
}

/* Periodic tick — expires pending past window, runs decay, tracks
 * lonely/starving sustain timers. Call under app->mtx. */
static void gotchi_tick(ClaudeBuddyApp* app) {
    uint32_t now = furi_get_tick();

    /* Expire pending beyond the window. */
    if(app->pending_msg_ts && now - app->pending_msg_ts > CLASSIFY_WINDOW_MS) {
        gotchi_classify_pending(app);
    }

    /* Decay once per minute. */
    if(!app->last_decay_ms) {
        app->last_decay_ms = now;
    } else if(now - app->last_decay_ms >= DECAY_TICK_MS) {
        app->play_level = (app->play_level > PLAY_DECAY_PER_MIN) ?
                              app->play_level - PLAY_DECAY_PER_MIN : 0;
        app->feed_level = (app->feed_level > FEED_DECAY_PER_MIN) ?
                              app->feed_level - FEED_DECAY_PER_MIN : 0;
        app->last_decay_ms = now;
    }

    /* Sustain timers. */
    bool low_low = app->play_level < PLAY_LOW && app->feed_level < FEED_LOW;
    if(low_low) {
        if(!app->lonely_since_ms) app->lonely_since_ms = now;
    } else {
        app->lonely_since_ms = 0;
    }

    if(app->feed_level == 0) {
        if(!app->starving_since_ms) app->starving_since_ms = now;
    } else {
        app->starving_since_ms = 0;
    }

    /* Autosave — every 5 min while running. Storage I/O is synchronous
     * from this timer-service task context; for 60 bytes it's fast
     * enough not to cause visible hitches. */
    if(!app->last_save_ms || now - app->last_save_ms >= AUTOSAVE_INTERVAL_MS) {
        persist_save(app);
    }
}

