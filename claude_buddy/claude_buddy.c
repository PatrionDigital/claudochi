/* Evolution thresholds calibrated for ~100 Claude Code messages/day:
 *   Egg → Child    after ~10 interactions (first hour of use)
 *   Child → Teen   after ~100 (first day)
 *   Teen → Adult   after ~1,000 (first ~10 days)
 *   Adult → Elder  after ~3,000 (first month)
 * Milestone celebrates fire on each transition. */
static PetEvoStage evo_stage_from_age(uint32_t age) {
    if(age >= 3000u) return EvoElder;
    if(age >= 1000u) return EvoAdult;
    if(age >= 100u) return EvoTeen;
    if(age >= 10u) return EvoChild;
    return EvoEgg;
}

/* 32×32 attention sprite per evo stage — used only in the Pokemon
 * modal's pet slot. Static icons (not IconAnimation) because the
 * modal always shows a single pose; no animation loop needed.
 * Subsampled from the corresponding 64×64 attention frame. */
static const Icon* attn_32_for_stage(PetEvoStage e) {
    switch(e) {
    case EvoEgg:   return &I_mascot_egg_attn_32x32;
    case EvoChild: return &I_mascot_child_attn_32x32;
    case EvoTeen:  return &I_mascot_teen_attn_32x32;
    case EvoAdult: return &I_mascot_adult_attn_32x32;
    case EvoElder: return &I_mascot_elder_attn_32x32;
    default:       return &I_mascot_child_attn_32x32;
    }
}

static const Icon* anim_icon_for_state(PetState s, PetEvoStage e) {
    if((unsigned)s >= PetStateCount || (unsigned)e >= EvoCount) {
        return &A_mascot_child_idle_64x64;
    }
    const Icon* i = mascot_icons[e][s];
    return i ? i : &A_mascot_child_idle_64x64;
}

/* IconAnimation's internal timer fires this callback on each frame
 * advance. We tell the GUI to redraw, which pulls the new frame via
 * canvas_draw_icon_animation in the draw callback. Runs on the Timer
 * Service task — view_port_update is thread-safe. */
static void anim_update_cb(IconAnimation* instance, void* ctx) {
    UNUSED(instance);
    ClaudeBuddyApp* app = ctx;
    view_port_update(app->view_port);
}

/* Swap to the animation for the given (state, stage) pair if it's
 * not already active. Must be called with app->mtx held. Swaps fire
 * when either axis changes — pet state OR life stage transition.
 *
 * Also detects FORWARD life-stage transitions (Egg→Child→Teen→...)
 * and kicks off the full-screen evolution cinematic. Backward moves
 * (e.g. Konami reset from Teen to Egg) deliberately don't trigger —
 * reset shouldn't celebrate regression. The evolve anim is allocated
 * here; the draw callback renders it full-screen and tears it down
 * when EVOLUTION_LINGER_MS expires. */
static void ensure_anim(ClaudeBuddyApp* app, PetState s) {
    PetEvoStage stage = evo_stage_from_age(app->age_transactions);

    /* Forward stage transition → fire evolution cinematic. Guarded
     * by anim_initialized so the first ensure_anim call after
     * startup / persist_load doesn't trigger on whatever stage the
     * saved age restored to. */
    if(app->anim_initialized && stage > app->anim_stage && !app->evolve_anim) {
        app->evolve_anim = icon_animation_alloc(&A_mascot_evolution_128x64);
        if(app->evolve_anim) {
            icon_animation_set_update_callback(app->evolve_anim, anim_update_cb, app);
            icon_animation_start(app->evolve_anim);
            app->evolve_until_ms = furi_get_tick() + EVOLUTION_LINGER_MS;
            /* Wake the backlight so the user doesn't miss the
             * cinematic if they've stepped away. Single-message
             * sequence (no delay phases), safe to call with the
             * app mutex held per Flipper's notification service. */
            if(app->notifications) {
                notification_message(
                    app->notifications, &sequence_display_backlight_on);
            }
        }
    }

    if(app->current_anim && app->anim_state == s && app->anim_stage == stage) {
        app->anim_initialized = true;
        return;
    }
    if(app->current_anim) {
        icon_animation_stop(app->current_anim);
        icon_animation_free(app->current_anim);
    }
    app->current_anim = icon_animation_alloc(anim_icon_for_state(s, stage));
    icon_animation_set_update_callback(app->current_anim, anim_update_cb, app);
    icon_animation_start(app->current_anim);
    app->anim_state = s;
    app->anim_stage = stage;
    app->anim_initialized = true;
}

/* Egg-crack progression within EvoEgg. Age < 10 keeps us in the Egg
 * stage; as the pet approaches hatching, cracks grow visible on the
 * shell so the user gets a progressive "something's about to happen"
 * cue rather than a silent jump from pristine-egg to Child sprite at
 * age=10. Returns the crack level 0/1/2:
 *   0 — pristine (age 0..3)
 *   1 — hairline single crack (age 4..6)
 *   2 — branching fork (age 7..9)
 * age >= 10 → out of Egg stage, caller should not be invoking this. */
static uint8_t egg_crack_level(uint32_t age) {
    if(age >= 7u) return 2;
    if(age >= 4u) return 1;
    return 0;
}

/* Paint the current mascot animation, then (only if we're still in
 * EvoEgg) layer the age-dependent crack overlay on top. Crack
 * positions picked so the lines fall on the egg's upper light surface
 * (y=16..23) without crossing the face features (eyes at y=28, mouth
 * at y=40). Caller must hold app->mtx — we only read animation /
 * stage / age fields. */
static void draw_mascot(Canvas* canvas, ClaudeBuddyApp* app) {
    if(!app->current_anim) return;
    canvas_draw_icon_animation(canvas, 0, 0, app->current_anim);
    if(app->anim_stage != EvoEgg) return;
    uint8_t lvl = egg_crack_level(app->age_transactions);
    if(lvl == 2) {
        canvas_draw_icon(canvas, 22, 16, &I_overlay_egg_crack_2_22x8);
    } else if(lvl == 1) {
        canvas_draw_icon(canvas, 24, 17, &I_overlay_egg_crack_1_18x6);
    }
}

/* ============================================================
 *  Heartbeat ingest
 * ============================================================ */

static void handle_rx_line(ClaudeBuddyApp* app, const char* line, size_t line_len) {
    jsmn_parser parser;
    jsmntok_t tokens[MAX_TOKENS];
    jsmn_init(&parser);
    int n = jsmn_parse(&parser, line, line_len, tokens, MAX_TOKENS);
    if(n < 1 || tokens[0].type != JSMN_OBJECT) return;

    furi_mutex_acquire(app->mtx, FuriWaitForever);

    int v;
    if((v = json_find_key(line, tokens, n, "total")) >= 0)
        app->hb_total = json_tok_int(line, &tokens[v]);
    if((v = json_find_key(line, tokens, n, "running")) >= 0)
        app->hb_running = json_tok_int(line, &tokens[v]);
    if((v = json_find_key(line, tokens, n, "waiting")) >= 0)
        app->hb_waiting = json_tok_int(line, &tokens[v]);
    if((v = json_find_key(line, tokens, n, "msg")) >= 0) {
        char prev_msg[sizeof(app->hb_msg)];
        strlcpy(prev_msg, app->hb_msg, sizeof(prev_msg));
        json_tok_strcpy(line, &tokens[v], app->hb_msg, sizeof(app->hb_msg));

        /* Classify the new msg. Unmatched patterns are treated as
         * Claudegotchi substrate: stamp activity, accrue
         * interaction_bytes, and open a pending classification window
         * for the play/feed split. Only count on actual change to
         * avoid double-counting when the desktop re-sends the same
         * msg on its 10s keepalive. */
        if(app->hb_msg[0] && strcmp(prev_msg, app->hb_msg) != 0) {
            ClaudeBuddyMsgSummary probe;
            summarize_msg(app->hb_msg, &probe);
            if(probe.label[0] == '\0') {
                uint32_t bytes = (uint32_t)strlen(app->hb_msg);
                app->last_activity_ms = furi_get_tick();
                app->interaction_bytes += bytes;
                gotchi_record_msg_change(app, bytes);
            }
        }
    }

    /* tokens — drives both the celebrate-at-milestone transient AND
     * feed credit. */
    if((v = json_find_key(line, tokens, n, "tokens")) >= 0) {
        int new_tokens = json_tok_int(line, &tokens[v]);

        /* Feed bump from tokens delta. Only after baseline is set —
         * otherwise the first heartbeat (which reports the session's
         * lifetime total, not a delta) would feed the pet based on
         * everything done before the app launched. Cap per heartbeat
         * so a single giant response doesn't saturate the bar. */
        if(app->tokens_baseline_set && new_tokens > app->hb_tokens) {
            uint32_t delta = (uint32_t)(new_tokens - app->hb_tokens);
            uint32_t gain = delta / FEED_TOKENS_PER_UNIT;
            bool capped = false;
            if(gain > FEED_GAIN_CAP_PER_HB) {
                gain = FEED_GAIN_CAP_PER_HB;
                capped = true;
            }
            app->feed_level += gain;
            clamp_level(&app->feed_level);
            /* Debug log visible via `ufbt cli` — lets us correlate
             * token bursts to feed bar movement without opening the
             * device. Grep for "DBG hb". */
            FURI_LOG_I(
                TAG,
                "DBG hb tok_delta=%lu gain=%lu%s feed=%lu/1000 play=%lu/1000 "
                "age=%lu running=%d waiting=%d",
                (unsigned long)delta,
                (unsigned long)gain,
                capped ? "(cap)" : "",
                (unsigned long)app->feed_level,
                (unsigned long)app->play_level,
                (unsigned long)app->age_transactions,
                app->hb_running,
                app->hb_waiting);
        }

        if(!app->tokens_baseline_set && new_tokens > 0) {
            /* First sighting — snap milestone so we don't fire
             * immediately for pre-existing tokens at app start. */
            app->last_celebrated_tokens =
                (new_tokens / TOKENS_MILESTONE) * TOKENS_MILESTONE;
            app->tokens_baseline_set = true;
        } else if(
            app->tokens_baseline_set &&
            new_tokens >= app->last_celebrated_tokens + (int)TOKENS_MILESTONE) {
            set_transient(app, PetStateCelebrate, CELEBRATE_LINGER_MS);
            app->last_celebrated_tokens =
                (new_tokens / TOKENS_MILESTONE) * TOKENS_MILESTONE;
        }
        app->hb_tokens = new_tokens;
    }

    ClaudeBuddyMode prev_mode = app->mode;
    int p_idx = json_find_key(line, tokens, n, "prompt");
    if(p_idx >= 0 && tokens[p_idx].type == JSMN_OBJECT) {
        if((v = json_find_key(line, tokens, n, "id")) >= 0)
            json_tok_strcpy(line, &tokens[v], app->prompt_id, sizeof(app->prompt_id));
        if((v = json_find_key(line, tokens, n, "tool")) >= 0)
            json_tok_strcpy(line, &tokens[v], app->prompt_tool, sizeof(app->prompt_tool));
        if((v = json_find_key(line, tokens, n, "hint")) >= 0)
            json_tok_strcpy(line, &tokens[v], app->prompt_hint, sizeof(app->prompt_hint));
        app->mode = ClaudeBuddyModePrompt;
    } else {
        /* Desktop cleared the prompt — drop the modal. */
        app->mode = ClaudeBuddyModeNormal;
    }
    bool entering_prompt = (prev_mode != ClaudeBuddyModePrompt) &&
                           (app->mode == ClaudeBuddyModePrompt);
    if(entering_prompt) {
        /* Stamp the time so the input handler can detect fast approval. */
        app->prompt_shown_at_ms = furi_get_tick();
        /* Reset cursor to ONCE (left cell) — safe default. A previous
         * DENY selection shouldn't persist into the next prompt and
         * risk an accidental reject on quick OK. */
        app->prompt_cursor_col = 0;
    }

    ensure_anim(app, pet_state_derive(app));

    furi_mutex_release(app->mtx);

    /* On the Normal → Prompt edge: buzz to grab attention, AND kick the
     * display backlight back on if it's gone to sleep — otherwise the
     * user might miss the modal entirely if they walked away. Don't
     * re-fire on subsequent heartbeats while already in the modal. */
    if(entering_prompt && app->notifications) {
        notification_message(app->notifications, &sequence_single_vibro);
        notification_message(app->notifications, &sequence_display_backlight_on);
    }

    view_port_update(app->view_port);
}

/* ============================================================
 *  RX drain thread
 * ============================================================ */

static int32_t claude_buddy_rx_thread(void* ctx) {
    ClaudeBuddyApp* app = ctx;
    uint8_t chunk[64];
    while(app->running) {
        size_t n =
            furi_stream_buffer_receive(app->rx_stream, chunk, sizeof(chunk), 250);
        for(size_t i = 0; i < n; i++) {
            if(app->line_len < LINE_BUF_SIZE - 1) {
                app->line_buf[app->line_len++] = chunk[i];
            } else {
                /* Over-long line — reset and drop until the next newline. */
                app->line_len = 0;
            }
            if(chunk[i] == '\n') {
                handle_rx_line(app, (const char*)app->line_buf, app->line_len);
                app->line_len = 0;
            }
        }
    }
    return 0;
}

/* ============================================================
 *  Drawing
 * ============================================================ */

static void claude_buddy_draw(Canvas* canvas, void* ctx) {
    ClaudeBuddyApp* app = ctx;

    /* Hold the mutex for the whole draw. We need current_anim to stay
     * valid while canvas_draw_icon_animation reads its frame data.
     * Swap-anim sites also hold the mutex, so they can't free
     * current_anim under us here. */
    furi_mutex_acquire(app->mtx, FuriWaitForever);

    /* Re-derive state every draw — picks up transient expiry
     * (heart/celebrate/reconnecting fading back) without needing the
     * swap sites to know about the timeout. ensure_anim is a no-op if
     * state didn't change, so cost is just the derive + a comparison. */
    ensure_anim(app, pet_state_derive(app));

    ClaudeBuddyMode mode = app->mode;
    BtStatus bt_status = app->bt_status;
    bool cccd = app->cccd_enabled;
    int t = app->hb_total, r = app->hb_running, w = app->hb_waiting;

    canvas_clear(canvas);

    /* Evolution cinematic takes over the whole canvas when a stage
     * transition is in flight. Runs for EVOLUTION_LINGER_MS, then
     * tears down the animation and falls through to normal
     * rendering (which now shows the NEW stage sprite). Modal
     * prompts that arrive during the cinematic queue implicitly —
     * the prompt state is set in heartbeat parsing, and the next
     * draw after the cinematic ends will flip into prompt mode. */
    if(app->evolve_anim) {
        uint32_t now = furi_get_tick();
        if(now < app->evolve_until_ms) {
            canvas_draw_icon_animation(canvas, 0, 0, app->evolve_anim);
            furi_mutex_release(app->mtx);
            return;
        }
        /* Expired — teardown so normal rendering resumes next. */
        icon_animation_stop(app->evolve_anim);
        icon_animation_free(app->evolve_anim);
        app->evolve_anim = NULL;
        app->evolve_until_ms = 0;
    }

    uint32_t now_ms = furi_get_tick();
    bool in_narration = app->narration_until_ms > now_ms;

    if(mode == ClaudeBuddyModePrompt || in_narration) {
        /* Pokemon-style battle modal:
         *   y=0..10   banner    "Wild <tool> appeared!"
         *   y=11      divider
         *   y=12..46  combat    "?" glyph (left) + 32×32 pet (right)
         *   y=47      divider
         *   y=48..63  actions   1-row × 2-col grid OR narration text
         *
         * Only ONCE + DENY are rendered because REFERENCE.md lists
         * only those as valid decisions. Left/Right moves cursor,
         * OK confirms. Up/Down have no effect (single-row). After
         * OK, narration replaces the action row for NARRATION_LINGER_MS
         * before the modal closes. */

        /* Banner */
        canvas_set_font(canvas, FontSecondary);
        char banner[56]; /* "Wild " + up to 32-char tool + " appeared!" + null */
        snprintf(banner, sizeof(banner), "Wild %s appeared!", app->prompt_tool);
        canvas_draw_str(canvas, 2, 9, banner);
        canvas_draw_line(canvas, 0, 11, 127, 11);

        /* Combat zone — tool glyph (left) + attention pet (right).
         * Tool glyph is the generic framed "?"; tool NAME sits in
         * the banner above, so the left slot stays stage-agnostic. */
        canvas_draw_icon(canvas, 16, 14, &I_icon_tool_32x32);
        canvas_draw_icon(canvas, 80, 14, attn_32_for_stage(app->anim_stage));

        canvas_draw_line(canvas, 0, 47, 127, 47);

        if(in_narration) {
            /* Narration replaces the grid: "Claudochi used X!" in two
             * lines because "APPROVE!"/"DENY!" in FontPrimary takes
             * enough horizontal space that a one-line "Claudochi used
             * DENY!" overruns 128 px. */
            canvas_set_font(canvas, FontSecondary);
            canvas_draw_str(canvas, 16, 55, "Claudochi used");
            canvas_set_font(canvas, FontPrimary);
            const char* used = "???";
            if(strcmp(app->narration_decision, "once") == 0) used = "APPROVE!";
            else if(strcmp(app->narration_decision, "deny") == 0) used = "DENY!";
            canvas_draw_str(canvas, 40, 63, used);
        } else {
            /* Action row — ONCE | DENY with vertical divider + cursor. */
            canvas_draw_line(canvas, 63, 48, 63, 63);
            canvas_set_font(canvas, FontSecondary);
            static const char* const LABELS[2] = {"ONCE", "DENY"};
            const int cell_xs[2] = {2, 66};
            const int label_xs[2] = {10, 74};
            const int by = 60;
            for(int c = 0; c < 2; c++) {
                if(c == app->prompt_cursor_col) {
                    canvas_draw_str(canvas, cell_xs[c], by, ">");
                }
                canvas_draw_str(canvas, label_xs[c], by, LABELS[c]);
            }
        }

        furi_mutex_release(app->mtx);
        return;
    }

    /* Normal mode: mascot carries the state signal (state-driven frames
     * cover sleep/idle/busy/attention, plus blue LED for connected).
     * Right column is deliberately minimal: just a title block and the
     * current msg. Removed BT status line + T/R/W counters — they
     * duplicated info the mascot already conveys, and the msg has
     * more room to breathe now. */
    (void)bt_status;
    (void)cccd;
    (void)t;
    (void)r;
    (void)w;

    draw_mascot(canvas, app);

    /* Title "Claudochi" in FontPrimary — Tamagotchi-ified brand mark.
     * Flipper's default fonts are ASCII-only (haxrcorp_4089_tr,
     * helvB08_tr), so Japanese くろどっち can't render without a custom
     * font; romanized "Claudochi" carries the same tamagotchi flavor
     * and fits the 62 px column where "Claudebuddy" clipped. */
    canvas_set_font(canvas, FontPrimary);
    canvas_draw_str(canvas, 66, 12, "Claudochi");

    /* Summarized msg: only recognized status patterns render. Free-form
     * text (including what the user is typing into Claude Code) is
     * deliberately NOT shown — bare user input on a pocket-sized screen
     * is a privacy leak. When an unrecognized msg recently arrived,
     * show a pulsing "…" instead to acknowledge "something's happening"
     * without surfacing the content. */
    ClaudeBuddyMsgSummary sum;
    summarize_msg(app->hb_msg, &sum);
    if(sum.label[0]) {
        canvas_set_font(canvas, FontPrimary);
        canvas_draw_str(canvas, 66, 36, sum.label);
        if(sum.detail[0]) {
            canvas_set_font(canvas, FontSecondary);
            canvas_draw_str(canvas, 66, 50, sum.detail);
        }
    } else {
        /* No known label → Claudegotchi panel, two-row per stat:
         *   row A: [10x10 glyph] Label text
         *   row B: gauge bar full-width
         * Stacked for Happy then Food, with Age on the last row.
         * Bars fill positively (high = good). */
        const int bx = 66, bw = 60, bh = 5;

        canvas_draw_icon(canvas, 66, 13, &I_icon_heart_10x10);
        canvas_set_font(canvas, FontSecondary);
        canvas_draw_str(canvas, 80, 22, "Happy");
        canvas_draw_frame(canvas, bx, 25, bw, bh);
        uint32_t pfill = (app->play_level * (bw - 2)) / LEVEL_MAX;
        if(pfill > 0) canvas_draw_box(canvas, bx + 1, 26, (int)pfill, bh - 2);

        canvas_draw_icon(canvas, 66, 33, &I_icon_burger_10x10);
        canvas_draw_str(canvas, 80, 42, "Food");
        canvas_draw_frame(canvas, bx, 45, bw, bh);
        uint32_t ffill = (app->feed_level * (bw - 2)) / LEVEL_MAX;
        if(ffill > 0) canvas_draw_box(canvas, bx + 1, 46, (int)ffill, bh - 2);

        /* Age readout on the last row + typing dots inline. */
        uint32_t now = furi_get_tick();
        bool typing = app->last_activity_ms && now - app->last_activity_ms < 2000u;
        char age_line[20];
        snprintf(
            age_line,
            sizeof(age_line),
            typing ? "Age:%lu ..." : "Age:%lu",
            (unsigned long)app->age_transactions);
        canvas_draw_str(canvas, 66, 60, age_line);
    }

    /* Power overlays: upper-right 8x8 corner.
     *   - Charging (USB plugged in): lightning bolt
     *   - Low battery (<20%): battery warning icon
     * Charging takes priority over low — if both (e.g. starting to
     * charge at 10%), user sees the "it's being fed" glyph first. */
    if(furi_hal_power_is_charging()) {
        canvas_draw_icon(canvas, 120, 0, &I_overlay_charging_8x8);
    } else if(furi_hal_power_get_pct() < LOW_BATTERY_PCT) {
        canvas_draw_icon(canvas, 120, 0, &I_overlay_hungry_8x8);
    }

    furi_mutex_release(app->mtx);
}

/* ============================================================
 *  Input + BT callbacks
 * ============================================================ */

static void claude_buddy_input(InputEvent* event, void* ctx) {
    ClaudeBuddyApp* app = ctx;
    furi_message_queue_put(app->input_queue, event, FuriWaitForever);
}

static void claude_buddy_redraw_tick(void* ctx) {
    ClaudeBuddyApp* app = ctx;
    /* Tamagotchi tick: decay, expire pending classifications, track
     * lonely/starving sustain timers. Runs at 4 Hz (REDRAW_PERIOD_MS)
     * so lonely/starving transitions are picked up within ~250ms. */
    furi_mutex_acquire(app->mtx, FuriWaitForever);
    gotchi_tick(app);
    furi_mutex_release(app->mtx);
    view_port_update(app->view_port);
}

static void claude_buddy_on_bt_status(BtStatus status, void* ctx) {
    ClaudeBuddyApp* app = ctx;
    furi_mutex_acquire(app->mtx, FuriWaitForever);
    BtStatus prev = app->bt_status;
    app->bt_status = status;

    /* Reconnecting transient: was Connected, now Advertising. */
    if(prev == BtStatusConnected && status == BtStatusAdvertising) {
        set_transient(app, PetStateReconnecting, RECONNECTING_LINGER_MS);
    }
    if(status == BtStatusConnected) {
        app->was_connected = true;
    }

    ensure_anim(app, pet_state_derive(app));
    furi_mutex_release(app->mtx);

    /* Blue LED while a central is connected, off otherwise — mirrors
     * hid_app/hid.c:100. Small quality-of-life indicator that matches
     * stock Flipper UX conventions for BLE apps. */
    if(app->notifications) {
        notification_message(
            app->notifications,
            status == BtStatusConnected ? &sequence_set_blue_255 : &sequence_reset_blue);
    }
}

static void claude_buddy_on_rx(const uint8_t* data, uint16_t len, void* ctx) {
    ClaudeBuddyApp* app = ctx;
    /* BLE thread context — push bytes and return fast. */
    furi_stream_buffer_send(app->rx_stream, data, len, 0);
}

static void claude_buddy_on_conn(bool connected, void* ctx) {
    ClaudeBuddyApp* app = ctx;
    furi_mutex_acquire(app->mtx, FuriWaitForever);
    app->cccd_enabled = connected;
    furi_mutex_release(app->mtx);
}

/* ============================================================
 *  Entry point
 * ============================================================ */

int32_t claude_buddy_app(void* p) {
    UNUSED(p);
    ClaudeBuddyApp* app = malloc(sizeof(ClaudeBuddyApp));
    memset(app, 0, sizeof(*app));

    app->mtx = furi_mutex_alloc(FuriMutexTypeNormal);
    app->input_queue = furi_message_queue_alloc(8, sizeof(InputEvent));
    app->rx_stream = furi_stream_buffer_alloc(RX_STREAM_SIZE, RX_STREAM_TRIGGER);
    app->running = true;
    app->notifications = furi_record_open(RECORD_NOTIFICATION);

    /* Restore persisted Tamagotchi stats before doing anything else —
     * the pet remembers across reboots. */
    persist_load(app);

    /* Start with the sleep animation — we haven't connected to anyone
     * yet and heartbeat hasn't been parsed. Will swap as state evolves. */
    furi_mutex_acquire(app->mtx, FuriWaitForever);
    ensure_anim(app, PetStateSleep);
    furi_mutex_release(app->mtx);

    app->view_port = view_port_alloc();
    view_port_draw_callback_set(app->view_port, claude_buddy_draw, app);
    view_port_input_callback_set(app->view_port, claude_buddy_input, app);
    app->gui = furi_record_open(RECORD_GUI);
    gui_add_view_port(app->gui, app->view_port, GuiLayerFullscreen);
    app->redraw_timer = furi_timer_alloc(claude_buddy_redraw_tick, FuriTimerTypePeriodic, app);
    furi_timer_start(app->redraw_timer, REDRAW_PERIOD_MS);

    /* RX drain thread */
    app->rx_thread = furi_thread_alloc_ex("ClaudeBuddyRx", RX_THREAD_STACK, claude_buddy_rx_thread, app);
    furi_thread_start(app->rx_thread);

    /* Bring up BLE */
    app->bt = furi_record_open(RECORD_BT);
    bt_set_status_changed_callback(app->bt, claude_buddy_on_bt_status, app);
    app->profile = bt_profile_start(app->bt, ble_profile_claude_buddy, NULL);
    if(!app->profile) {
        FURI_LOG_E(TAG, "bt_profile_start failed");
    } else {
        claude_buddy_profile_set_rx_callback(app->profile, claude_buddy_on_rx, app);
        claude_buddy_profile_set_conn_callback(app->profile, claude_buddy_on_conn, app);
    }

    /* Main input loop */
    InputEvent event;
    while(furi_message_queue_get(app->input_queue, &event, FuriWaitForever) == FuriStatusOk) {
        if(event.type != InputTypeShort) continue;

        /* Back always exits, regardless of mode. */
        if(event.key == InputKeyBack) break;

        /* Snapshot mode + prompt id for the handler below. */
        furi_mutex_acquire(app->mtx, FuriWaitForever);
        ClaudeBuddyMode mode = app->mode;
        char id[48];
        strlcpy(id, app->prompt_id, sizeof(id));
        bool narration_active = app->narration_until_ms > furi_get_tick();

        /* Narration phase swallows all input — Pokemon-game feel:
         * text box plays out to completion. Back still exits (handled
         * above this block). */
        if(narration_active) {
            furi_mutex_release(app->mtx);
            continue;
        }

        /* Konami-code reset — Up Up Down Down Left Right Left Right.
         * Only when NOT in a prompt (so Left in prompt mode still
         * means deny, not just konami progress). Running match;
         * any non-matching key that's also not the start resets. */
        bool konami_fired = false;
        if(mode != ClaudeBuddyModePrompt) {
            if(event.key == KONAMI_CODE[app->konami_progress]) {
                app->konami_progress++;
                if(app->konami_progress >= KONAMI_LEN) {
                    claudegotchi_reset(app);
                    konami_fired = true;
                }
            } else {
                app->konami_progress = (event.key == KONAMI_CODE[0]) ? 1 : 0;
            }
        }

        furi_mutex_release(app->mtx);

        /* Reset side-effects fire outside the mutex — see the
         * comment above claudegotchi_reset for why. persist_save
         * touches Storage (can block on SD), double_vibro blocks on
         * the buzzer sequence, and backlight_on kicks the display
         * awake for visual confirmation. All three are safe to call
         * without the app mutex because claudegotchi_reset already
         * settled the in-memory state. */
        if(konami_fired) {
            persist_save(app);
            if(app->notifications) {
                notification_message(app->notifications, &sequence_double_vibro);
                notification_message(app->notifications, &sequence_display_backlight_on);
            }
            view_port_update(app->view_port);
        }

        if(mode != ClaudeBuddyModePrompt) continue;
        if(id[0] == '\0') continue;

        /* Pokemon nav: Left/Right cycle cursor between ONCE (col=0)
         * and DENY (col=1). Up/Down no-op. OK confirms current cell. */
        if(event.key == InputKeyLeft || event.key == InputKeyRight) {
            furi_mutex_acquire(app->mtx, FuriWaitForever);
            if(event.key == InputKeyLeft && app->prompt_cursor_col > 0) {
                app->prompt_cursor_col--;
            } else if(event.key == InputKeyRight && app->prompt_cursor_col < 1) {
                app->prompt_cursor_col++;
            }
            furi_mutex_release(app->mtx);
            view_port_update(app->view_port);
            continue;
        }

        if(event.key != InputKeyOk) continue;

        /* OK confirms — decision from current cursor column. */
        furi_mutex_acquire(app->mtx, FuriWaitForever);
        const char* decision =
            (app->prompt_cursor_col == 0) ? "once" : "deny";
        furi_mutex_release(app->mtx);

        bool tx_ok = false;
        if(app->profile) {
            char json[160];
            int n = snprintf(
                json,
                sizeof(json),
                "{\"cmd\":\"permission\",\"id\":\"%s\",\"decision\":\"%s\"}\n",
                id,
                decision);
            if(n > 0 && (size_t)n < sizeof(json)) {
                tx_ok = claude_buddy_profile_tx(
                    app->profile, (const uint8_t*)json, (uint16_t)n);
            }
        }

        /* Dismiss modal locally regardless of TX result — immediate
         * visual ack via the narration phase. Also update streak
         * counters + heart-transient here so the pet's mood
         * reflects the decision immediately.
         *
         * Deliberately do NOT overwrite hb_msg with "sent: X (ok)"
         * — the narration already shows "Claudochi used APPROVE!"
         * and after that expires the home screen should resume its
         * normal bars/age panel rather than holding a stale status
         * line until the next heartbeat. If TX failed, the desktop
         * will re-send the prompt and the modal will naturally
         * reopen after narration expires. */
        furi_mutex_acquire(app->mtx, FuriWaitForever);
        app->mode = ClaudeBuddyModeNormal;
        app->prompt_id[0] = '\0';
        (void)tx_ok;
        /* Kick off the narration phase — modal stays visible showing
         * "Claudochi used X!" for NARRATION_LINGER_MS even though
         * mode already flipped to Normal. Draw path gates on
         * narration_until_ms in addition to mode == Prompt. */
        uint32_t now = furi_get_tick();
        strlcpy(app->narration_decision, decision, sizeof(app->narration_decision));
        app->narration_until_ms = now + NARRATION_LINGER_MS;

        /* Approval = anything but deny. Derived from the decision
         * string because OK now confirms whichever cell the cursor
         * is on, not just ONCE. */
        bool approved = strcmp(decision, "deny") != 0;
        if(approved) {
            app->approval_streak++;
            app->denial_streak = 0;
            app->total_approvals++;
            /* Approval feeds the pet — explicit intent to run the
             * tool = food. Separate from tokens-delta feed so the
             * user sees immediate response to their decision,
             * independent of how big the resulting tool run is. */
            app->feed_level += FEED_PER_APPROVAL;
            clamp_level(&app->feed_level);
            /* Heart if approval happened within 5s of the prompt arriving. */
            if(app->prompt_shown_at_ms &&
               now - app->prompt_shown_at_ms < HEART_TRIGGER_MS) {
                set_transient(app, PetStateHeart, HEART_LINGER_MS);
            }
        } else {
            app->denial_streak++;
            app->approval_streak = 0;
            app->total_denials++;
        }

        ensure_anim(app, pet_state_derive(app));
        furi_mutex_release(app->mtx);
        view_port_update(app->view_port);
    }

    /* Teardown: stop drain thread, then tear down BLE, then GUI. */
    app->running = false;
    furi_thread_join(app->rx_thread);
    furi_thread_free(app->rx_thread);

    /* Flush Tamagotchi stats to SD before losing anything else. */
    persist_save(app);

    if(app->profile) {
        bt_profile_restore_default(app->bt);
    }
    bt_set_status_changed_callback(app->bt, NULL, NULL);
    furi_record_close(RECORD_BT);

    /* Make sure the LED is off when we leave. */
    notification_message(app->notifications, &sequence_reset_blue);
    furi_record_close(RECORD_NOTIFICATION);

    furi_timer_stop(app->redraw_timer);
    furi_timer_free(app->redraw_timer);
    gui_remove_view_port(app->gui, app->view_port);
    view_port_free(app->view_port);
    furi_record_close(RECORD_GUI);

    /* Animation after view_port — the icon-animation timer could have
     * fired a view_port_update between our gui_remove_view_port and
     * view_port_free. Stopping the animation first avoids that race. */
    if(app->current_anim) {
        icon_animation_stop(app->current_anim);
        icon_animation_free(app->current_anim);
    }
    /* Evolution cinematic may still be in flight on teardown (user
     * hit Back mid-animation). Same timer-service race applies. */
    if(app->evolve_anim) {
        icon_animation_stop(app->evolve_anim);
        icon_animation_free(app->evolve_anim);
    }

    furi_stream_buffer_free(app->rx_stream);
    furi_message_queue_free(app->input_queue);
    furi_mutex_free(app->mtx);
    free(app);
    return 0;
}
