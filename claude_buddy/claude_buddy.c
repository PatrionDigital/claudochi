/* ============================================================
 *  JSON helpers — small wrappers on jsmn
 * ============================================================ */

static bool tok_streq(const char* json, const jsmntok_t* t, const char* s) {
    int len = t->end - t->start;
    return (int)strlen(s) == len && strncmp(json + t->start, s, len) == 0;
}

/* Linear key search: find the first STRING token matching `key` and
 * return the index of the immediately following (value) token. Good
 * enough for the Hardware Buddy heartbeat since all keys we care about
 * are globally unique across the object (total/running/waiting/msg at
 * top level; id/tool/hint only under `prompt`). Returns -1 if not
 * found. */
static int json_find_key(const char* json, const jsmntok_t* tokens, int n, const char* key) {
    for(int i = 0; i < n - 1; i++) {
        if(tokens[i].type == JSMN_STRING && tok_streq(json, &tokens[i], key)) {
            return i + 1;
        }
    }
    return -1;
}

static int json_tok_int(const char* json, const jsmntok_t* t) {
    char buf[16] = {0};
    int len = t->end - t->start;
    if(len < 0 || len >= (int)sizeof(buf)) return 0;
    memcpy(buf, json + t->start, len);
    return atoi(buf);
}

static void
    json_tok_strcpy(const char* json, const jsmntok_t* t, char* dst, size_t dst_size) {
    int len = t->end - t->start;
    if(len < 0) len = 0;
    if(len >= (int)dst_size) len = dst_size - 1;
    memcpy(dst, json + t->start, len);
    dst[len] = '\0';
}

/* Summarize a free-form heartbeat `msg` string into a short label
 * (≤ 5 chars) plus an optional detail word. The desktop's msg field is
 * "one-line summary suitable for a small display" per REFERENCE.md, but
 * in practice still too long for 64 px of right-column font-secondary.
 * Common observed patterns:
 *     "running: yarn test"      → RUN / yarn
 *     "approve: Bash"           → ASK / Bash
 *     "called bash"             → RUN / bash
 *     "done(success), 1 turns"  → DONE
 *     "done(failure), …"        → FAIL
 *     "approved"                → OK
 *     <anything else>           → raw msg in detail, no label
 * Extend the ladder here as new desktop patterns surface. */
typedef struct {
    char label[8];    /* 1-5 char summary, rendered in FontPrimary */
    char detail[24];  /* optional trailing word(s), FontSecondary */
} ClaudeBuddyMsgSummary;

static void copy_first_word(const char* src, char* dst, size_t dst_size) {
    while(*src == ' ') src++;
    size_t n = 0;
    while(*src && *src != ' ' && n < dst_size - 1) {
        dst[n++] = *src++;
    }
    dst[n] = '\0';
}

static void summarize_msg(const char* raw, ClaudeBuddyMsgSummary* s) {
    s->label[0] = '\0';
    s->detail[0] = '\0';
    if(!raw || !raw[0]) return;

    /* Desktop sometimes wraps msg in parens, e.g. "(called Bash)".
     * Strip a leading "(" so pattern matching works uniformly. */
    const char* p = raw;
    if(*p == '(') p++;

    if(strncmp(p, "running:", 8) == 0) {
        strlcpy(s->label, "RUN", sizeof(s->label));
        copy_first_word(p + 8, s->detail, sizeof(s->detail));
    } else if(strncmp(p, "approve:", 8) == 0) {
        strlcpy(s->label, "ASK", sizeof(s->label));
        copy_first_word(p + 8, s->detail, sizeof(s->detail));
    } else if(strncmp(p, "called ", 7) == 0) {
        strlcpy(s->label, "RUN", sizeof(s->label));
        copy_first_word(p + 7, s->detail, sizeof(s->detail));
    } else if(strncmp(p, "done(success", 12) == 0) {
        strlcpy(s->label, "DONE", sizeof(s->label));
    } else if(
        strncmp(p, "done(failure", 12) == 0 || strncmp(p, "done(error", 10) == 0 ||
        strncmp(p, "failed", 6) == 0) {
        strlcpy(s->label, "FAIL", sizeof(s->label));
    } else if(strcmp(p, "approved") == 0) {
        strlcpy(s->label, "OK", sizeof(s->label));
    } else if(strcmp(p, "denied") == 0) {
        strlcpy(s->label, "NO", sizeof(s->label));
    } else {
        /* Unknown pattern — show the raw text truncated, no label. */
        strlcpy(s->detail, raw, sizeof(s->detail));
    }

    /* Trim a trailing ")" that the paren-wrap leaves behind in detail. */
    size_t n = strlen(s->detail);
    if(n > 0 && s->detail[n - 1] == ')') s->detail[n - 1] = '\0';
}

/* Full pet state derivation — priority order (first match wins):
 *    1. Attention   — prompt pending
 *    2. Transient   — heart / celebrate / reconnecting within linger
 *    3. Starving    — feed == 0 for STARVING_SUSTAIN_MS (critical)
 *    4. Sleep       — BT not connected
 *    5. Overloaded  — running > 0 && total >= 5
 *    6. Busy        — running > 0
 *    7. Grumpy      — denial_streak >= GRUMPY_STREAK  (short-horizon UX)
 *    8. Happy       — approval_streak >= HAPPY_STREAK (short-horizon UX)
 *    9. Content     — play & feed both high           (long-horizon mood)
 *   10. Focused     — feed high, play low             (long-horizon mood)
 *   11. Lonely      — both low for LONELY_SUSTAIN_MS  (long-horizon mood)
 *   12. Idle        — connected with sessions
 *   13. Sleep       — default (no sessions)
 * Caller must hold app->mtx. */
static PetState pet_state_derive(const ClaudeBuddyApp* app) {
    if(app->mode == ClaudeBuddyModePrompt || app->hb_waiting > 0) {
        return PetStateAttention;
    }
    uint32_t now = furi_get_tick();
    if(app->transient_until_ms && now < app->transient_until_ms) {
        return app->transient_state;
    }
    if(app->starving_since_ms && now - app->starving_since_ms > STARVING_SUSTAIN_MS) {
        return PetStateStarving;
    }
    if(app->bt_status != BtStatusConnected) return PetStateSleep;
    if(app->hb_running > 0) {
        return (app->hb_total >= 5) ? PetStateOverloaded : PetStateBusy;
    }
    if(app->denial_streak >= GRUMPY_STREAK) return PetStateGrumpy;
    if(app->approval_streak >= HAPPY_STREAK) return PetStateHappy;
    if(app->play_level >= PLAY_HIGH && app->feed_level >= FEED_HIGH) return PetStateContent;
    if(app->feed_level >= FEED_HIGH && app->play_level < PLAY_LOW) return PetStateFocused;
    if(app->play_level < PLAY_LOW && app->feed_level < FEED_LOW &&
       app->lonely_since_ms && now - app->lonely_since_ms > LONELY_SUSTAIN_MS) {
        return PetStateLonely;
    }
    return (app->hb_total > 0) ? PetStateIdle : PetStateSleep;
}

static void set_transient(ClaudeBuddyApp* app, PetState s, uint32_t linger_ms) {
    app->transient_state = s;
    app->transient_until_ms = furi_get_tick() + linger_ms;
}

/* =======================================================================
 *  Claudegotchi classification + decay
 * ======================================================================= */

static void clamp_level(uint32_t* lvl) {
    if(*lvl > LEVEL_MAX) *lvl = LEVEL_MAX;
}

/* Classify the pending msg change: feed if tokens grew during the
 * window, play if not. Clears the pending slot. Also bumps age, and
 * fires the Celebrate transient at milestone ages (10, 50, 200, 1000). */
static void gotchi_classify_pending(ClaudeBuddyApp* app) {
    if(!app->pending_msg_ts) return;
    int tokens_delta = app->hb_tokens - app->pending_start_tokens;
    uint32_t credit = app->pending_msg_bytes;
    if(credit > MSG_CLASSIFY_CAP) credit = MSG_CLASSIFY_CAP;
    if(tokens_delta > 0) {
        app->feed_level += credit;
        clamp_level(&app->feed_level);
    } else {
        app->play_level += credit;
        clamp_level(&app->play_level);
    }
    app->pending_msg_ts = 0;
    app->pending_msg_bytes = 0;

    /* Age bump. Each classified interaction is one life experience.
     * Milestone celebrate fires at each evolution boundary — the
     * transition is already visible (new sprite set), the
     * double-length Celebrate adds a bit of fanfare. */
    uint32_t age = ++app->age_transactions;
    if(age == 10u || age == 100u || age == 1000u || age == 3000u) {
        set_transient(app, PetStateCelebrate, CELEBRATE_LINGER_MS * 2u);
    }
}

